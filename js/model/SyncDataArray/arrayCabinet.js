function arrayCabinet(str)
{
    const arrayCabinet = [];
    arrayCabinet.push(Translate.translate('Название проекта')+' : '+str);
    arrayCabinet.push(Translate.translate('Правильная установка названия шкафа'));
    arrayCabinet.push(Translate.translate('Название шкафа соответствует названию на наклейке со штрих-кодом.'));
    arrayCabinet.push(Translate.translate('Цвет гравировки данные'));
    return arrayCabinet;
}
